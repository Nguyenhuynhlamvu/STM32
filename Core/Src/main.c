float calculate_velocity(GPIO_TypeDef *GPIOx, uint16_t GPIO_Pin, uint8_t pin)
{
	current_time_in_us[pin] = __HAL_TIM_GetCounter(&htim2);
	elapsedtime = (current_time_in_100ms - last_time_in_100ms[pin]) * 10000 + current_time_in_us[pin] - last_time_in_us[pin];
	last_time_in_100ms[pin] = current_time_in_100ms;
	last_time_in_us[pin] = current_time_in_us[pin];
	
	if (HAL_GPIO_ReadPin(GPIOx, GPIO_Pin) == 1)
	{
		vel_fb[pin] = 2*M_PI / 249.6 / elapsedtime * 100000 ;
		
	}
	else
	{
		vel_fb[pin] = -2*M_PI / 249.6 / elapsedtime * 100000 ;
	}	
	return vel_fb[pin];
}

